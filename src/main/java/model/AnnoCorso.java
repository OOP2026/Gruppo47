package model;

/**
 * Rappresenta l'anno accademico a cui è associato un {@link Insegnamento}
 * o a cui è iscritto uno {@link Studente}.
 * <p>
 * L'enumerazione copre la durata standard di un corso di laurea triennale.
 * </p>
 */
public enum AnnoCorso {

    /**
     * Primo anno di corso.
     */
    I,

    /**
     * Secondo anno di corso.
     */
    II,

    /**
     * Terzo anno di corso.
     */
    III;
}